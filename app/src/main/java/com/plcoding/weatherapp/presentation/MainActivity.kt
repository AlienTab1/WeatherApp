@AndroidEntryPoint
class MainActivity : ComponentActivity() {
    private val viewModel: WeatherViewModel by viewModels()
    private lateinit var permissionLauncher: ActivityResultLauncher<Array<String>>

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)

        permissionLauncher = registerForActivityResult(
            ActivityResultContracts.RequestMultiplePermissions()
        ) {
            viewModel.loadWeatherInfo()
        }
        permissionLauncher.launch(arrayOf(
            Manifest.permission.ACCESS_FINE_LOCATION,
            Manifest.permission.ACCESS_COARSE_LOCATION,
        ))

        setContent {
                        WeatherAppTheme {
                Box(
                    modifier = Modifier
                        .fillMaxWidth()
                        //.padding(16.dp),

                ) {
                    /*
                    Button(onClick = {
                        println("Local cliked")
                        val intent = Intent(applicationContext, LocalWeatherDetailActivity::class.java)
                        intent.putExtra("weather_data", viewModel.state)
                        applicationContext.startActivity(intent)
                        //val intent = Intent(context, MyTargetActivity::class.java)
                        //intent.putExtra("people_data", student)
                        //context.startActivity(intent)

                    }) {
                        Text(text = "Local")
                    }

                     */

                    Column(
                        modifier = Modifier
                            .fillMaxSize()
                            .background(DarkBlue)
                    ) {
                        WeatherCard(
                            state = viewModel.state,
                            backgroundColor = DeepBlue
                        )
                        Spacer(modifier = Modifier.height(16.dp))
                        WeatherForecast(state = viewModel.state)
                        Spacer(modifier = Modifier.height(16.dp))
                        WeatherWeeklyForecast(state = viewModel.state)
                    }

                    if(viewModel.state.isLoading) {
                        CircularProgressIndicator(
                            modifier = Modifier.align(Alignment.Center)
                        )
                    }
                    viewModel.state.error?.let { error ->
                        Text(
                            text = error,
                            color = Color.Red,
                            textAlign = TextAlign.Center,
                            modifier = Modifier.align(Alignment.Center)
                        )
                    }
                }
            }
        }
    }
}


